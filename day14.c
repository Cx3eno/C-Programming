#include <stdio.h>
// Typecasting syntax
// (type) value; <- (int) value
/* What is typecasting?
Typecasting - refers to converting a data type to a different data type
Typeconversion - refers to converting of calculation between int-int which gives int value
and between int-float gives float value and between float-float gives float value and
if the user wants the float value between int-int then convert any int to float */

int main(int argc, char const *argv[])
{
    int a = 4;
    float b = (float)54 / 5;

    printf("The value of a is %f\n", b);
    return 0;
}
